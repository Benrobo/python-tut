
# Colors utility


def Red(text):
    return print("\033[91m {}\033[00m".format(text))

def Green(text): 
    return print("\033[92m {}\033[00m" .format(text))

def Yellow(text): 
    return print("\033[93m {}\033[00m" .format(text))

def LightPurple(text): 
    return print("\033[94m {}\033[00m" .format(text))

def Purple(text): 
	return print("\033[95m {}\033[00m" .format(text))

def Cyan(text): 
	return print("\033[96m {}\033[00m" .format(text))

def LightGray(text): 
	return print("\033[97m {}\033[00m" .format(text))

def Black(text): 
	return print("\033[98m {}\033[00m" .format(text))

